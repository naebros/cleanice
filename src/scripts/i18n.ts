export type I18nKey = keyof typeof i18n.en;

export function replaceYear(text: string): string {
  return text.replace(/\{year\}/g, String(new Date().getFullYear()));
}

export function getLang(): string {
  return localStorage.getItem('cleanice_lang') || 'en';
}

export function t(key: I18nKey): string {
  const lang = getLang();
  const val = i18n[lang as keyof typeof i18n]?.[key] ?? i18n.en[key] ?? key;
  return replaceYear(val);
}

function smoothText(el: Element, finalText: string, delay: number) {
  setTimeout(() => {
    (el as HTMLElement).style.transition = 'filter .15s ease-out, opacity .15s ease-out';
    (el as HTMLElement).style.filter = 'blur(4px)';
    (el as HTMLElement).style.opacity = '.4';
    setTimeout(() => {
      el.textContent = finalText;
      (el as HTMLElement).style.filter = '';
      (el as HTMLElement).style.opacity = '';
      (el as HTMLElement).style.transition = '';
    }, 150);
  }, delay);
}

export function setLang(lang: string) {
  localStorage.setItem('cleanice_lang', lang);
  document.documentElement.lang = lang === 'ru' ? 'ru' : 'en';
  document.querySelectorAll('[data-i18n]').forEach((el, i) => {
    const key = el.getAttribute('data-i18n') as I18nKey;
    const raw = i18n[lang as keyof typeof i18n]?.[key];
    if (raw && raw !== el.textContent) smoothText(el, replaceYear(raw), i * 25);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el, i) => {
    const key = el.getAttribute('data-i18n-placeholder') as I18nKey;
    const val = i18n[lang as keyof typeof i18n]?.[key];
    if (val) setTimeout(() => { (el as HTMLInputElement).placeholder = replaceYear(val); }, i * 25);
  });
  const langBtn = document.getElementById('langSwitcher');
  if (langBtn) {
    smoothText(langBtn, lang === 'en' ? 'EN' : 'RU', 0);
    langBtn.setAttribute('aria-label', lang === 'en' ? 'Language: English' : 'Language: Русский');
  }
}

export function getTheme(): string {
  return localStorage.getItem('cleanice_theme') || 'dark';
}

export function setTheme(theme: string) {
  localStorage.setItem('cleanice_theme', theme);
  document.body.classList.toggle('light', theme === 'light');
  document.querySelector('meta[name="theme-color"]')?.setAttribute('content', theme === 'light' ? '#F5F3EF' : '#090909');
}

export function initI18n() {
  if ((window as any).__i18nObs) {
    (window as any).__i18nObs.disconnect();
    (window as any).__i18nObs = null;
  }
  setTheme(getTheme());

  document.getElementById('langSwitcher')?.addEventListener('click', () => {
    setLang(getLang() === 'en' ? 'ru' : 'en');
  });
  document.getElementById('themeSwitcher')?.addEventListener('click', () => {
    setTheme(getTheme() === 'dark' ? 'light' : 'dark');
  });
}
